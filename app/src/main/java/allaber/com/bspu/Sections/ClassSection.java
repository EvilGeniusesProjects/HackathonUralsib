package allaber.com.bspu.Sections;

public class ClassSection {
    String image;
    String name;
    String description;
    String supervisor;
    String place;
    String date;
    String email;
    String phone;
    String vk;

    public ClassSection() {
    }

    public ClassSection(String image, String name, String description, String supervisor, String place, String date, String email, String phone, String vk){
        this.image = image;
        this.name = name;
        this.description = description;
        this.supervisor = supervisor;
        this.place = place;
        this.date = date;
        this.email = email;
        this.phone = phone;
        this.vk = vk;



    }
}
