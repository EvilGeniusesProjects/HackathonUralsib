package allaber.com.bspu.Timetable;

public class ClassTimetable {

    private String dayOfTheWeek; //День недели
    private String couple; //Пара
    private String coupleTime; //Время пары
    private String itemName; //Название предмета
    private String aWeek; //Неделя
    private String subgroup; //Подгруппа
    private String kindOfCouple; //Вид пары
    private String theAudience; //Аудитория
    private String teacher; //Преподаватель
    private String coupleTimeStart; //Начало пары
    private String coupleTimeStartEnd; //Конец пары


    public String getDayOfTheWeek() {
        return dayOfTheWeek;
    }

    public void setDayOfTheWeek(String dayOfTheWeek) {
        this.dayOfTheWeek = dayOfTheWeek;
    }

    public String getCouple() {
        return couple;
    }

    public void setCouple(String couple) {
        this.couple = couple;
    }

    public String getCoupleTime() {
        return coupleTime;
    }

    public void setCoupleTime(String coupleTime) {
        this.coupleTime = coupleTime;
    }

    public String getItemName() {
        return itemName;
    }

    public void setItemName(String itemName) {
        this.itemName = itemName;
    }

    public String getaWeek() {
        return aWeek;
    }

    public void setaWeek(String aWeek) {
        this.aWeek = aWeek;
    }

    public String getSubgroup() {
        return subgroup;
    }

    public void setSubgroup(String subgroup) {
        this.subgroup = subgroup;
    }

    public String getKindOfCouple() {
        return kindOfCouple;
    }

    public void setKindOfCouple(String kindOfCouple) {
        this.kindOfCouple = kindOfCouple;
    }

    public String getTheAudience() {
        return theAudience;
    }

    public void setTheAudience(String theAudience) {
        this.theAudience = theAudience;
    }

    public String getTeacher() {
        return teacher;
    }

    public void setTeacher(String teacher) {
        this.teacher = teacher;
    }

    public String getCoupleTimeStart() {
        return coupleTimeStart;
    }

    public void setCoupleTimeStart(String coupleTimeStart) {
        this.coupleTimeStart = coupleTimeStart;
    }

    public String getCoupleTimeStartEnd() {
        return coupleTimeStartEnd;
    }

    public void setCoupleTimeStartEnd(String coupleTimeStartEnd) {
        this.coupleTimeStartEnd = coupleTimeStartEnd;
    }
}
